import json
import logging

from pydantic import ValidationError

from .mongo import MongoManager
from .collect import Collector
from .models.dinnerly import Recipe
from .models.dinnerme import DinnerMe, Ingredient

logger = logging.getLogger()


class Manager:
    def __init__(self, mongo: MongoManager) -> None:
        self.mongo = mongo
        self.collector = Collector()

    @staticmethod
    def dinnerly_to_dinnerme(recipe: Recipe) -> DinnerMe:
        dinnerme = DinnerMe(**recipe.dict())
        dinnerme.set_ingredients()
        dinnerme.set_servings()
        return dinnerme

    async def run(self, n_weeks: int = 1):
        logger.info(f"Collecting the next {n_weeks} weeks of recipes")
        weekly_menus = self.collector.get_menus(n_weeks)
        for menu in weekly_menus:
            for recipe_card in menu.recipes:
                if recipe_card.image.url is None:
                    continue
                if await self.mongo.recipe_exists(recipe_card):
                    logger.info(f"Recipe '{recipe_card.title}' with id: {recipe_card.id} already exists, passing")
                    continue

                try:
                    logger.info(f"Fetching recipe id: {recipe_card.id}")
                    recipe = self.collector.get_recipe(recipe_card.slug, recipe_card.id)
                    recipe.startOfWeek = menu.startOfWeek
                    dinnerme = Manager.dinnerly_to_dinnerme(recipe)
                except (json.decoder.JSONDecodeError, ValidationError):
                    logger.error(f"Failed to fetch recipe id: {recipe_card.id}", exc_info=True)
                else:
                    await self.mongo.insert_recipe(dinnerme)

